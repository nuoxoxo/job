#include "ft_utils.h"

int     main(int argc, char **argv)
{
        char    *c;
        int     L;
        int     R;

        if (argc != 4)  return (0);
        c = argv[2];
        L = ft_atoi(argv[1]);
        R = ft_atoi(argv[3]);
        calc(L, R, c);
        write(1, "\n", 1);
        return (0);
}
