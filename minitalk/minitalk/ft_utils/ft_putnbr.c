#include "../minitalk.h"

void	ft_putnbr(int num)
{
	unsigned int	n;
	char			c;

	n = num;
	if (num < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
	}
	c = n % 10 + 48;
	write(1, &c, 1);
}
